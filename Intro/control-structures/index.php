<?php

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// IF / ELSE statements

// the name of the user
$name = "Peter";

// the age of the user
$age = 28;

// whether the user in an adult or not
$isAnAdult = false;


// first example
if($age >= 18){
    $isAnAdult = true;
    print "$name is $age years old. Is he an adult? Yes he is";
} else {
    $isAnAdult = false;
    print "$name is $age years old. Is he an adult? No he isn't";
}


// in some cases, there's an easier way to check
$isAnAdult = $age >= 18;
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Ternary operator | is something true ? if true : if not true

print "$name is $age years old. Is he an adult? " . ($age >= 18 ? "yes he is" : 'no he\'s not');

print "$name is $age years old. Is he an adult? " . ($isAnAdult ? "yes he is" : 'no he\'s not');
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////



?>